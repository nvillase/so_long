#include "so_long.h"

void	calcul(t_l *l)
{
	int	q;

	l->m = l->th / 2;
	q = l->m / 2;
	l->x = (q / 2) + 2;
}

int	n_c(t_l *l)
{
	int	i;
	int	j;

	l->cn = 0;
	i = 0;
	j = 0;
	while (l->map[i])
	{
		while (l->map[i][j])
		{
			if (l->map[i][j] == 'C')
				l->cn++;
			j++;
		}
		j = 0;
		i++;
	}
	return (0);
}
