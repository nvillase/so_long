#include "so_long.h"

void secu(t_l *l)
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (i < l->tv)
	{
		while (j < l->th)
		{
			if (i)
			j++;
		}
		i++;
		j = 0;
	}
}